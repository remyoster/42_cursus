#include <stdlib.h>
#include "libft.h"

void	*ft_memalloc(size_t size)
{
	void	*mem;

	if (!(mem = (void*)malloc(sizeof(size_t) * size)))
		return (NULL);
	mem = ft_memset(mem, 0, size);
	return (mem);
}
