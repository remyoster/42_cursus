#include <string.h>

int		ft_strcmp(char const *s1, char const *s2)
{
	size_t i;

	i = 0;
	while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0')
		i++;
	if (s1[i] < 0 || s2[i] < 0)
		return (-(s1[i] - s2[i]));
	return (s1[i] - s2[i]);
}
